    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        String s = (String)(jComboBox1.getSelectedItem());
        
        if(s.equals("Random")){
            int n = (int)(Math.random() * 8 + 3); 
            int [][] nums = new int [n] [n];
            nums = fillArray(n,nums);
            for (int i = 0; i < n; i++) {
                for (int j = 0; j < n; j++) {
                    if(nums[i][j] > 9)
                        W.append(nums[i][j] + " ");
                    else
                        W.append(" " + nums[i][j] + " ");
                }
                W.append("\n");
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        XYSeries series = new XYSeries("Largest");
      
        for (int i = 5; i < 500; i *= 2) {
            int [][] D = new int [i * 16][i * 16];
            D = fillArray(D.length , D);
            
            long start = System.currentTimeMillis();
            Floyd(D.length , D);
            long end = System.currentTimeMillis();
            long time = end - start;
            
            series.add(D.length,time);
        }
        
        XYSeriesCollection dataset = new XYSeriesCollection();
        dataset.addSeries(series);
        JFreeChart chart = ChartFactory.createXYLineChart("Largest" ,
                "ArraySize", "Time ns" , dataset ,PlotOrientation.VERTICAL,false
        ,true , true);
        
        ChartPanel chartPanel = new ChartPanel(chart);
        chartPanel.setPreferredSize(new java.awt.Dimension(500, 270));
        
        JFrame plotJFrame = new JFrame ("plot");
        plotJFrame.setContentPane(chartPanel);
        plotJFrame.setSize(500, 500);
        plotJFrame.setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

