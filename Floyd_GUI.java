    public static int [][] fillArray(int n , int [][] W){
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                if(i == j)
                    W[j][i] = 0;
                else
                    W[i][j] = (int)(Math.random() * 100);
                
            }
        }
        return W;
    }
